        "import pandas as pd\n",
        "import numpy as np\n",
        "import tensorflow as tf\n",
        "from tensorflow.keras.datasets import fashion_mnist\n",
        "import os\n",
        "from ydata_profiling import ProfileReport  # Use ydata-profiling for EDA\n",
        "\n",
        "def load_fashion_mnist():\n",
        "    \"\"\"This function is to Load Fashion MNIST dataset as DataFrame.\"\"\"\n",
        "    (X_train, y_train), (X_test, y_test) = fashion_mnist.load_data()\n",
        "\n",
        "    # Normalize the pixel values to be between 0 and 1\n",
        "    X_train_normalized = X_train.astype('float32') / 255.0\n",
        "    X_test_normalized = X_test.astype('float32') / 255.0\n",
        "\n",
        "    # Reduce the number of features by taking a smaller subset of pixels (e.g., only a few rows of pixels)\n",
        "    # We'll select the top-left 14x14 pixels instead of using all 28x28 pixels\n",
        "    X_train_reduced = X_train_normalized[:, :14, :14]\n",
        "    X_test_reduced = X_test_normalized[:, :14, :14]\n",
        "\n",
        "    # Flatten the reduced images\n",
        "    X_train_flat = X_train_reduced.reshape(X_train_reduced.shape[0], -1)\n",
        "    X_test_flat = X_test_reduced.reshape(X_test_reduced.shape[0], -1)\n",
        "\n",
        "    # Convert the data into the DataFrame\n",
        "    columns = [f'pixel_{i}' for i in range(X_train_flat.shape[1])]\n",
        "    df_train = pd.DataFrame(X_train_flat, columns=columns)\n",
        "    df_train['label'] = y_train.astype(int)\n",
        "\n",
        "    df_test = pd.DataFrame(X_test_flat, columns=columns)\n",
        "    df_test['label'] = y_test.astype(int)\n",
        "\n",
        "    return df_train, df_test\n",
        "\n",
        "def get_key_insights(df):\n",
        "    \"\"\"Extract key insights from the dataset.\"\"\"\n",
        "    data_insights = {}\n",
        "\n",
        "    # Verify Class Distribution\n",
        "    class_counts = df['label'].value_counts(normalize=True) * 100\n",
        "    imbalance = class_counts.max() - class_counts.min()\n",
        "    data_insights['class_distribution'] = f\"Class imbalance: {imbalance:.2f}%\"\n",
        "\n",
        "    # Find if any Missing Values are there\n",
        "    missing_values = df.isnull().sum().sum()\n",
        "    data_insights['missing_values'] = f\"Total missing values found are: {missing_values}\"\n",
        "\n",
        "    # Identify Low-Variance Features\n",
        "    feature_variances = df.drop(columns=['label']).var()\n",
        "    low_variance_features = feature_variances[feature_variances < 1.0].index.tolist()\n",
        "    data_insights['low_variance'] = f\"Low variance features are : {len(low_variance_features)}\"\n",
        "\n",
        "    # Identify Highly Correlated Features\n",
        "    corr_matrix = df.drop(columns=['label']).corr().abs()\n",
        "    high_corr_features = [(i, j, corr_matrix.loc[i, j]) for i in corr_matrix.columns for j in corr_matrix.columns if i != j and corr_matrix.loc[i, j] > 0.9]\n",
        "    data_insights['high_correlation'] = f\"Highly correlated features are: {len(high_corr_features)}\"\n",
        "\n",
        "    return data_insights\n",
        "\n",
        "def generate_eda_report(df, output_path, sample_size=100):  # Reduced sample size to 100\n",
        "    \"\"\"This function is used to Generate an EDA report.\"\"\"\n",
        "\n",
        "    df_sample = df.sample(n=min(len(df), sample_size), random_state=42)\n",
        "\n",
        "    # Ensure 'label' is numeric\n",
        "    df_sample['label'] = df_sample['label'].astype(int)\n",
        "\n",
        "    # Generate EDA Report using ydata-profiling with minimal output and more optimizations\n",
        "    profile = ProfileReport(\n",
        "        df_sample,\n",
        "        title=\"******EDA Report for Fashion MNIST Dataset ******\",\n",
        "        explorative=True,\n",
        "        minimal=True,\n",
        "        correlations=None,  # Disable correlation calculations\n",
        "        missing_diagrams=None,  # Disable missing value diagrams\n",
        "        interactions=None  # Disable interaction plots\n",
        "    )\n",
        "    profile.to_file(output_path)\n",
        "    print(f\"EDA reports are saved at: {output_path}\")\n",
        "\n",
        "def main():\n",
        "    \"\"\"Main function to generate EDA reports and extract important insights for the dataset\"\"\"\n",
        "    df_train, df_test = load_fashion_mnist()\n",
        "\n",
        "    output_dir = \"EDA_Reports\"\n",
        "    os.makedirs(output_dir, exist_ok=True)\n",
        "\n",
        "    train_report_path = os.path.join(output_dir, \"train_fashion_mnist_eda_report.html\")\n",
        "    test_report_path = os.path.join(output_dir, \"test_fashion_mnist_eda_report.html\")\n",
        "\n",
        "    print(\"********* Generating EDA reports for Fashion MNIST dataset *******\")\n",
        "    generate_eda_report(df_train, train_report_path)\n",
